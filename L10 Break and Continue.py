## break and continue

# break

print('break a loop')

a=[1,2,3,4,5]
for i in a :
    if i==4:
        break
    else:
        print()


a=('red apple')
for i in a:
    if i=="p":
        break
    else:print(i,end="")
print()


# continue

print('continue a loop')
a=[1,2,3,4,5]
for i in a :
    if i==3:
        continue
    else:print(i)


 













